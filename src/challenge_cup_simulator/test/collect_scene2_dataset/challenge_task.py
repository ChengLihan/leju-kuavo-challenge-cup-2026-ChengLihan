def _package_root():
    return os.path.abspath(os.path.join(os.path.dirname(os.path.abspath(__file__)), ".."))


def _find_sim_pkg_path():
    try:
        import rospkg
        return rospkg.RosPack().get_path("challenge_cup_simulator")
    except Exception:
        return os.path.abspath(os.path.join(_package_root(), "..", "challenge_cup_simulator"))


def _yaw_from_quat_wxyz(quat_wxyz):
    qw, qx, qy, qz = [float(v) for v in quat_wxyz]
    norm = math.sqrt(qw * qw + qx * qx + qy * qy + qz * qz)
    if norm > 1e-9:
        qw, qx, qy, qz = qw / norm, qx / norm, qy / norm, qz / norm
    return math.atan2(
        2.0 * (qw * qz + qx * qy),
        1.0 - 2.0 * (qy * qy + qz * qz),
    )


def _fmt_float(value):
    return f"{float(value):.9f}"


def _save_scene2_part_layout(scene, seed):
    """导出 Scene2 当前 seed 的随机工件布局。

    自动数采脚本需要知道每个工件的真实 world 坐标和姿态，用于生成抓取点。
    challenge_secret 只在仿真启动侧知道随机布局，所以这里在启动 Scene2 前
    把布局写成 YAML，供 `scene2_part_grasp_ik.py` 按 seed 读取。
    """

    if scene != "scene2":
        return

    sim_pkg_path = _find_sim_pkg_path()
    lib_dir = os.path.join(sim_pkg_path, "lib")
    if lib_dir not in sys.path:
        sys.path.insert(0, lib_dir)

    try:
        from challenge_secret import get_object_layout
    except ImportError as exc:
        print(f"[WARN] challenge_task: 无法导出 scene2 工件布局，未找到 challenge_secret: {exc}")
        return

    layout = get_object_layout(scene, seed)
    if not layout:
        print(f"[WARN] challenge_task: 无法导出 scene2 工件布局，seed={seed} 返回空布局")
        return

    missing = [name for name in SCENE2_PART_NAMES if name not in layout]
    if missing:
        print(f"[WARN] challenge_task: scene2 布局缺少工件 {missing}，跳过布局配置保存")
        return

    # 统一写到数采目录，避免旧的 test/generated_layouts 与新目录混用。
    output_dir = os.path.join(sim_pkg_path, "test", "collect_scene2_dataset", "generated_layouts")
    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, f"{scene}_seed_{seed}_parts.yaml")

    lines = [
        "# Auto-generated by challenge_task.py. Do not edit by hand.",
        f"scene: {scene}",
        f"seed: {seed}",
        "parts:",
    ]
    for name in SCENE2_PART_NAMES:
        od = layout[name]
        pos = [float(v) for v in od["pos"]]
        quat = [float(v) for v in od["quat"]]
        yaw_z_rad = _yaw_from_quat_wxyz(quat)
        lines.extend([
            f"  {name}:",
            "    world_xyz: [" + ", ".join(_fmt_float(v) for v in pos) + "]",
            "    quat_wxyz: [" + ", ".join(_fmt_float(v) for v in quat) + "]",
            f"    yaw_z_rad: {_fmt_float(yaw_z_rad)}",
            f"    yaw_z_deg: {_fmt_float(math.degrees(yaw_z_rad))}",
        ])

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")

    print(f"[INFO] challenge_task: scene2 工件布局配置已保存: {output_path}")


def _load_launcher():
    # 公共启动器位于受保护包 challenge_cup_simulator/utils/（选手不可改动），
    # 从那里导入，确保完整性校验无法被绕过。
    try:
        import rospkg
        sim_utils = os.path.join(rospkg.RosPack().get_path("challenge_cup_simulator"), "utils")
    except Exception:
        sim_utils = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                                 "..", "..", "challenge_cup_simulator", "utils")
    sys.path.insert(0, sim_utils)
    from challenge_sim_launcher import ChallengeSimLauncher
    return ChallengeSimLauncher


